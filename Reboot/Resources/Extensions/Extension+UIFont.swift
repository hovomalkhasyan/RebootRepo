import Foundation
import UIKit

extension UIFont {
    class func GiloryMadium(size: CGFloat) -> UIFont {
        return UIFont(name: "Gilroy-Medium", size: size)!
    }
    class func GiloryUltraLightItalic(size: CGFloat) -> UIFont {
        return UIFont(name: "Gilroy-UltraLightItalic", size: size)!
    }
    class func GilroyBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Gilroy-Bold", size: size)!
    }
    class func  GilroyRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "Gilroy-Regular", size: size)!
    }
}
